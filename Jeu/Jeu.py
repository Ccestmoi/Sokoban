"""
Box : Caisse  dplacer par le dmnageur.
    Etant donn qu'une caisse doit tre dplac, le canvas et la matrice sont necessaires pour
    reconstruire l'image et mettre en oeuvre sont dplacement (dans le canvas et dans la matrice)
    Un Box est "movable", il est toujours dssin au dessus des objets "static" :
        Le zOrder est assur par le tag du create_image (tag='movable')
        et self.canvas.tag_raise("movable","static") dans Level
    Un Box est reprsent differemment (image diffrente) suivant qu'il se situe sur un emplacement marqu par un Goal ou non.
 """


class Box(object):
    def __init__(self, canvas, wharehouse, position, onGoal=False):
        self.width = 64
        self.height = 64
        self.canvas = canvas
        self.position = position
        self.wharehouse = wharehouse
        self.onGoal = onGoal
        self.image = tk.PhotoImage(file='box.png')
        self.imageId = self.canvas.create_image(
            self.position.getX() * self.width + self.width / 2,
            self.position.getY() * self.height + self.height / 2,
            image=self.image,
            tags="movable"
        )

    def getWidth(self):
        return self.width

    def getHeight(self):
        return self.height

    def isMovable(self):
        return True

    def canBeCovered(self):
        return False

    def moveTowards(self, direction):
        nextPos = self.position.positionTowards(direction, 1)
        nextElement = self.wharehouse.at(nextPos)
        if nextElement is None or not nextElement.canBeCovered():
            return False

        # Mise  jour de la matrice
        self.wharehouse.atPut(self.position, Floor())
        self.position = nextPos
        self.wharehouse.atPut(self.position, self)

        # Mise  jour de l'tat onGoal
        self.updateOnGoalStatus()

        # Mise  jour de l'affichage
        canvasPos = self.position.asCanvasPositionIn(self)
        self.canvas.coords(
            self.imageId,
            canvasPos.getX() + self.width / 2,
            canvasPos.getY() + self.height / 2
        )
        return True

    def updateOnGoalStatus(self):
        currentElement = self.wharehouse.at(self.position)
        self.onGoal = isinstance(currentElement, Goal)
        self.updateImage()

    def updateImage(self):
        if self.onGoal:
            self.image = tk.PhotoImage(file='box_on_goal.png')
        else:
            self.image = tk.PhotoImage(file='box.png')
        self.canvas.itemconfig(self.imageId, image=self.image)

    def xsbChar(self):
        return '*' if self.onGoal else '$'

    def isFreePlace(self):
        return False


"""
Mover : C'est  le dmnageur.
    La classe Mover met en oeuvre la logique du jeu dans #canMove et #moveTowards.
    Etant donn qu'un Mover se dplace, le canvas et la matrice sont necessaires pour
    reconstruire l'image et mettre en oeuvre sont dplacement (dans le canvas et dans la matrice)
    Un Mover est "movable", il est toujours dssin au dessus des objets "static" :
        Le zOrder est assur par le tag du create_image (tag='movable')
        et self.canvas.tag_raise("movable","static") dans Level
    Un Box est reprsent differemment (image diffrente) suivant la direction de dplacement (mme si le dpplacement s'avre impossible).
"""


class Mover(object):
    def __init__(self, canvas, wharehouse, position, onGoal=False):
        self.canvas = canvas
        self.wharehouse = wharehouse
        self.position = position
        self.onGoal = onGoal
        self.width = 64
        self.height = 64
        self.image = tk.PhotoImage(file='player.png')
        self.player = self.canvas.create_image(
            self.position.getX() * self.width + self.width / 2,
            self.position.getY() * self.height + self.height / 2,
            image=self.image,
            tags="movable"
        )

    def getWidth(self):
        return self.width

    def getHeight(self):
        return self.height

    def isMovable(self):
        return True

    def canMove(self, direction):
        nextPos = self.position.positionTowards(direction, 1)
        if not self.wharehouse.isPositionValid(nextPos):
            return False
        nextElement = self.wharehouse.at(nextPos)

        if isinstance(nextElement, (Floor, Goal)):
            return True
        elif isinstance(nextElement, Box):
            boxNextPos = nextPos.positionTowards(direction, 1)
            if not self.wharehouse.isPositionValid(boxNextPos):
                return False
            boxNextElement = self.wharehouse.at(boxNextPos)
            if isinstance(boxNextElement, (Floor, Goal)):
                return True
        return False

    def moveTowards(self, direction):
        if not self.canMove(direction):
            return

        nextPos = self.position.positionTowards(direction, 1)
        nextElement = self.wharehouse.at(nextPos)

        if isinstance(nextElement, Box):
            nextElement.moveTowards(direction)

        # Mise  jour de la matrice
        self.wharehouse.atPut(self.position, Floor())
        self.position = nextPos
        self.wharehouse.atPut(self.position, self)

        # Mise  jour de l'affichage
        canvasPos = self.position.asCanvasPositionIn(self)
        self.canvas.coords(
            self.player,
            canvasPos.getX() + self.width / 2,
            canvasPos.getY() + self.height / 2
        )

    def setupImageForDirection(self, direction):
        if direction == Direction.Up:
            self.image = tk.PhotoImage(file='playerUp.png')
        elif direction == Direction.Down:
            self.image = tk.PhotoImage(file='playerDown.png')
        elif direction == Direction.Left:
            self.image = tk.PhotoImage(file='playerLeft.png')
        elif direction == Direction.Right:
            self.image = tk.PhotoImage(file='playerRight.png')

        self.canvas.itemconfig(self.player, image=self.image)

    def push(self, direction):
        self.setupImageForDirection(direction)
        self.moveTowards(direction)

    def xsbChar(self):
        return '+' if self.onGoal else '@'

    def isFreePlace(self):
        return False


"""
    Le jeux avec tout ce qu'il faut pour dessiner et stocker/grer la matrice d'lments
"""


class Level(object):
    def __init__(self, root, xsbMatrix):
        self.root = root
        self.wharehouse = WharehousePlan()

        # calcul des dimensions de la matrice
        nbrows = len(xsbMatrix)
        nbcolumns = 0

        for line in xsbMatrix:
            nbc = len(line)
            if nbc > nbcolumns:
                nbcolumns = nbc

        self.height = nbrows * 64
        self.width = nbcolumns * 64

        self.canvas = tk.Canvas(self.root, width=self.width, height=self.height, bg="gray")
        self.canvas.pack()

        self.initWharehouseFromXsb(xsbMatrix)
        self.root.bind("<Key>", self.keypressed)

    def initWharehouseFromXsb(self, xsbMatrix):
        # legend :
        #   '#' = wall,  '$' = box, '.' = goal, '*' = box on goal, '@' = mover, '+' = mover on goal, '-' = floor, ' ' = floor
        y = 0
        # Ajout ligne par ligne
        for lineIdx in range(len(xsbMatrix)):
            row = []
            x = 0
            # Ajout colonne par colonne
            for elemIdx in range(len(xsbMatrix[lineIdx])):
                e = xsbMatrix[lineIdx][elemIdx]
                pos = Position(x, y)
                # Ajoute le bon lment  la bonne position
                if e == '#':
                    # Ajout d'un mur
                    wall = Wall(self.canvas, pos)
                    row.append(wall)
                elif e == '@':
                    # Ajout du dmnageur
                    mover = Mover(self.canvas, self.wharehouse, pos, False)
                    row.append(mover)
                    self.wharehouse.setMover(mover)
                elif e == '$':
                    # Ajout d'une boite
                    box = Box(self.canvas, self.wharehouse, pos, False)
                    row.append(box)
                elif e == '.':
                    # Ajout des objectifs
                    goal = Goal(self.canvas, pos)
                    row.append(goal)
                else:
                    # Ajout d'une case vide
                    floor = Floor()
                    row.append(floor)
                x += 1
            self.wharehouse.appendRow(row)
            y += 1

        self.canvas.tag_raise("movable", "static")

    def keypressed(self, event):
        mover = self.wharehouse.getMover()
        if mover is None:
            return
    
        if event.keysym == 'Up':
            mover.push(Direction.Up)
        elif event.keysym == 'Down':
            mover.push(Direction.Down)
        elif event.keysym == 'Left':
            mover.push(Direction.Left)

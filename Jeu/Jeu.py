def xsbMatrix(matrix):
    # legend :
    #   '#' = wall,  '$' = box, '.' = goal, '*' = box on goal, '@' = mover,
    #   '+' = mover on goal, '-' = floor, ' ' = floor
    xsbMat = []
    for line in matrix:
        s = []
        for e in line:
            if e == None:
                s.append(' ')
            else:
                s.append(e.xsbChar())
        xsbMat.append(s)
    return xsbMat


def printXsbMatrix(matrix):
    l = 0
    for line in matrix:
        s = str(l) + ' : '
        for c in line:
            s = s + c
        print(s)
        l = l + 1
    print('')


